export class Operations {
  sum(firstNumber: number, secondNumber: number) {
    const sumResult = firstNumber + secondNumber
    return sumResult
  }

  subtraction(firstNumber: number, secondNumber: number) {
    const subtractionResult = firstNumber - secondNumber
    return subtractionResult
  }

  multiplication(firstNumber: number, secondNumber: number) {
    const multiResult = firstNumber * secondNumber
    return multiResult
  }

  division(firstNumber: number, secondNumber: number) {
    const divisionResult = firstNumber / secondNumber
    return divisionResult
  }
}